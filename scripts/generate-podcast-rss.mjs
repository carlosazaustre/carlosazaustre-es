#!/usr/bin/env node
/**
 * generate-podcast-rss.mjs
 *
 * Generates public/podcast.xml from content/podcast/*.mdx files.
 * Includes iTunes namespace fields for compatibility with podcast readers.
 *
 * Usage:
 *   node scripts/generate-podcast-rss.mjs
 *
 * Runs automatically via "prebuild" hook in package.json.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const PODCAST_DIR = path.join(ROOT, "content", "podcast");
const OUTPUT = path.join(ROOT, "public", "podcast.xml");
const BASE_URL = "https://carlosazaustre.es";

function escapeXml(str) {
  return String(str ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

function extractExcerpt(content, maxLength = 300) {
  const clean = content
    .replace(/!\[.*?\]\(.*?\)/g, "")
    .replace(/\[([^\]]+)\]\(.*?\)/g, "$1")
    .replace(/#{1,6}\s+/g, "")
    .replace(/`{1,3}[\s\S]*?`{1,3}/g, "")
    .replace(/\*{1,2}([^*]+)\*{1,2}/g, "$1")
    .replace(/_{1,2}([^_]+)_{1,2}/g, "$1")
    .replace(/<[^>]+>/g, "")
    // Remove transcript section — too noisy for RSS description
    .replace(/#{1,3}\s*Transcripci[oó]n completa[\s\S]*/i, "")
    .replace(/\n+/g, " ")
    .trim();

  if (clean.length <= maxLength) return clean;
  const cut = clean.lastIndexOf(" ", maxLength);
  return clean.slice(0, cut > 0 ? cut : maxLength) + "…";
}

function buildPodcastRss(episodes) {
  const lastBuildDate =
    episodes.length > 0
      ? new Date(episodes[0].date).toUTCString()
      : new Date().toUTCString();

  const items = episodes.map(({ slug, title, date, excerpt, tags, videoId, episodeNumber }) => {
    const url = `${BASE_URL}/podcast/${slug}`;
    const pubDate = new Date(date).toUTCString();
    const thumbnail = videoId
      ? `https://img.youtube.com/vi/${videoId}/maxresdefault.jpg`
      : `${BASE_URL}/carlos-azaustre.jpg`;
    const epNum = episodeNumber ? ` (Ep. ${episodeNumber})` : "";
    const cats = (Array.isArray(tags) ? tags : [])
      .map((t) => `    <category>${escapeXml(t)}</category>`)
      .join("\n");

    return `  <item>
    <title>${escapeXml(title)}</title>
    <link>${url}</link>
    <guid isPermaLink="true">${url}</guid>
    <description>${escapeXml(excerpt)}</description>
    <pubDate>${pubDate}</pubDate>
    <dc:creator>Carlos Azaustre</dc:creator>
    <itunes:title>${escapeXml(title)}</itunes:title>
    <itunes:author>Carlos Azaustre</itunes:author>
    <itunes:summary>${escapeXml(excerpt)}</itunes:summary>
    <itunes:episode>${episodeNumber ?? ""}</itunes:episode>
    <itunes:episodeType>full</itunes:episodeType>
    <itunes:explicit>false</itunes:explicit>
    <itunes:image href="${thumbnail}"/>
    <enclosure url="${thumbnail}" type="image/jpeg" length="0"/>
${cats}
  </item>`;
  }).join("\n");

  return `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-podcast-rss.mjs — do not edit manually -->
<rss version="2.0"
  xmlns:atom="http://www.w3.org/2005/Atom"
  xmlns:dc="http://purl.org/dc/elements/1.1/"
  xmlns:itunes="http://www.itunes.com/dtds/podcast-1.0.dtd"
  xmlns:content="http://purl.org/rss/1.0/modules/content/">
  <channel>
    <title>¿Qué TECH Cuentas? — Podcast de Carlos Azaustre</title>
    <link>${BASE_URL}/podcast</link>
    <description>Conversaciones con referentes del sector tech en España. Tecnología, programación, emprendimiento e innovación.</description>
    <language>es</language>
    <lastBuildDate>${lastBuildDate}</lastBuildDate>
    <atom:link href="${BASE_URL}/podcast.xml" rel="self" type="application/rss+xml"/>
    <image>
      <url>${BASE_URL}/carlos-azaustre.jpg</url>
      <title>¿Qué TECH Cuentas? — Podcast</title>
      <link>${BASE_URL}/podcast</link>
    </image>
    <itunes:author>Carlos Azaustre</itunes:author>
    <itunes:owner>
      <itunes:name>Carlos Azaustre</itunes:name>
      <itunes:email>cazaustre@gmail.com</itunes:email>
    </itunes:owner>
    <itunes:image href="${BASE_URL}/carlos-azaustre.jpg"/>
    <itunes:category text="Technology"/>
    <itunes:explicit>false</itunes:explicit>
    <itunes:type>episodic</itunes:type>
    <copyright>© ${new Date().getFullYear()} Carlos Azaustre</copyright>
    <managingEditor>cazaustre@gmail.com (Carlos Azaustre)</managingEditor>
${items}
  </channel>
</rss>`;
}

function run() {
  if (!fs.existsSync(PODCAST_DIR)) {
    console.warn(`⚠️  Podcast dir not found: ${PODCAST_DIR} — skipping podcast RSS`);
    return;
  }

  const files = fs
    .readdirSync(PODCAST_DIR)
    .filter((f) => f.endsWith(".md") || f.endsWith(".mdx"));

  const episodes = files
    .map((filename) => {
      const slug = filename.replace(/\.(md|mdx)$/, "");
      const raw = fs.readFileSync(path.join(PODCAST_DIR, filename), "utf8");
      const { data, content } = matter(raw);

      return {
        slug,
        title: data.title ?? "Sin título",
        date: data.date ? String(data.date) : "1970-01-01",
        excerpt: data.excerpt ?? extractExcerpt(content),
        tags: Array.isArray(data.tags) ? data.tags : [],
        videoId: data.videoId ? String(data.videoId) : null,
        episodeNumber: data.episodeNumber ? Number(data.episodeNumber) : null,
      };
    })
    .filter((p) => p.date && !isNaN(new Date(p.date).getTime()))
    .sort((a, b) => new Date(b.date) - new Date(a.date));

  const xml = buildPodcastRss(episodes);

  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, xml, "utf8");

  console.log(`✅  Podcast RSS generado → public/podcast.xml  (${episodes.length} episodios)`);
}

run();
